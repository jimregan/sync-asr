def get_corrections():
    corrections = {}
    for line in _CORRECTIONS.split("\n"):
        if line == "":
            continue
        parts = line.split()
        if not parts[0] in corrections:
            corrections[parts[0]] = []
        corrections[parts[0]].append(parts[1])
    return corrections


